'''표준 입력으로 나이(만 나이)가 입력됩니다.
교통카드 시스템에서 시내버스 요금은 다음과 같으며 각 나이에 맞게 요금을 차감한 뒤 잔액이 출력되게 만드세요(if, elif 사용).
현재 교통카드에는 9,000원이 들어있습니다.
•어린이(초등학생, 만 7세 이상 12세 이하): 350원
•청소년(중∙고등학생, 만 13세 이상 18세 이하): 560원
•어른(일반, 만 19세 이상): 1,100원'''

age = int(input())
balance = 9000
if age >= 7 and age <= 12:
	balance -= 350
elif age >= 13 and age <= 18:
	balance -= 560
elif age >= 19:
	balance -= 1100
print(balance)
